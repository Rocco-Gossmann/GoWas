func (s *debugScene) Unload(e *core.Engine) *struct{} {
	s.mapText = nil
	return nil
}

func (s *debugScene) Draw(e *core.Engine, ca *core.Canvas) {
	ca.FillColorA(0x00333333, 0x80, core.CANV_CL_ALL)

	s.mapText.ToCanvas(ca, &gfx.ToCanvasOpts{
		Scroll: types.Point{Y: uint16(s.tma), X: uint16(s.tma)},
	})

	mouse := ca.Mouse
	if mouse.X > 0 || mouse.Y > 0 {
		ca.Blit(&core.BlitSettings{
			Bmp: s.cursorBMP,
			X:   int32(mouse.X),
			Y:   int32(mouse.Y),
		})
	}
}

