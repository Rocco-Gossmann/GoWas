var firstDraw = true
var tma = 0.1

func (s *debugScene) Draw(e *core.Engine, ca *core.Canvas) {
	ca.FillColorA(0x00333333, 0x10, core.CANV_CL_ALL)

	//s.tsFont.BlitTo(ca, 1, &gfx.TilesetBlitOptions{
	//	X: -7,
	//	Y: -7,
	//}) // Print A
	if firstDraw {
		s.mapText.ToCanvas(ca, &gfx.ToCanvasOpts{
			Scroll: types.Point{Y: uint16(tma), X: uint16(tma)},
		})
		firstDraw = true
		tma++
	}

	mouse := ca.Mouse
	if mouse.X > 0 || mouse.Y > 0 {
		ca.Blit(&core.BlitSettings{
			Bmp: s.cursorBMP,
			X:   int32(mouse.X),
			Y:   int32(mouse.Y),
		})
	}
}

